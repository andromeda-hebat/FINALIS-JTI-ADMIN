package andromeda.hebat.finalisjtiadmin.models;

public class VerifTA {
    private int no;
    private String nim;
    private String nama_lengkap;
    private String aktivitas;
    private String tanggal_request;


    public int getNo() {
        return no;
    }

    public void setNo(int no) {
        this.no = no;
    }

    public String getNim() {
        return nim;
    }

    public void setNim(String nim) {
        this.nim = nim;
    }

    public String getNama_lengkap() {
        return nama_lengkap;
    }

    public void setNama_lengkap(String nama_lengkap) {
        this.nama_lengkap = nama_lengkap;
    }

    public String getAktivitas() {
        return aktivitas;
    }

    public void setAktivitas(String aktivitas) {
        this.aktivitas = aktivitas;
    }

    public String getTanggal_request() {
        return tanggal_request;
    }

    public void setTanggal_request(String tanggal_request) {
        this.tanggal_request = tanggal_request;
    }
}
